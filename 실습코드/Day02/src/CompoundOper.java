public class CompoundOper {
    public static void main(String[] args) {
        int a = 10;
        System.out.println("초기값 : " + a);

        a += 5; // a = a + 5
        System.out.println(a);

        a -= 3; // a = a - 3
        System.out.println(a);

        a *= 2; // a = a * 2
        System.out.println(a);

        a /= 4; // a = a / 4
        System.out.println(a);

        a %= 5; // a = a % 5
        System.out.println(a);
    }
}
