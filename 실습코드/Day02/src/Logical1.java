public class Logical1 {
    public static void main(String[] args) {
        System.out.println(10 > 3 && 3 == 3);
        System.out.println(10 < 3 && 3 == 3);
        System.out.println(10 < 3 || 1 != 1);
        System.out.println(10 > 3 || 1 != 1);
        System.out.println(true);
        System.out.println(!true);
    }
}
