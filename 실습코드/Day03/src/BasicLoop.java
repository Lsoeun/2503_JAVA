public class BasicLoop {
    // 반복문을 사용하지 않아 번거로운 코드
    // 1부터 10까지의 합을 구합니다.
    public static void main(String[] args) {
        int num = 1;
        num += 2; // num = num + 2
        num += 3; // num = num + 3
        num += 4; // num = num + 4
        num += 5; // num = num + 5
        num += 6; // num = num + 6
        num += 7; // num = num + 7
        num += 8; // num = num + 8
        num += 9; // num = num + 9
        num += 10; // num = num + 10
        System.out.println("1부터 10까지의 합은" + num + "입니다.");
    }
}
